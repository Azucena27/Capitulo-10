package ejercicio1;

/**
 *
 * @author Azucena
 */
public class Horse {

    public String nombre;
    public String color;
    public String añoNaci;

    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the color
     */
    public String getColor() {
        return color;
    }

    /**
     * @param color the color to set
     */
    public void setColor(String color) {
        this.color = color;
    }

    /**
     * @return the añoNaci
     */
    public String getAñoNaci() {
        return añoNaci;
    }

    /**
     * @param añoNaci the añoNaci to set
     */
    public void setAñoNaci(String añoNaci) {
        this.añoNaci = añoNaci;
    }

}
